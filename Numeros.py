# numeros inteiros
print(2+3)
print(3 - 2)
print(2 * 3)
print(3 / 2)
print('\n')

# Exponenciacao
print(3 ** 2)
print(3 ** 3)
print(10 ** 6)
print('\n')

# ordem de operacoes
print(2 + 3 * 4)
print((2+3) * 4)
print('\n')

# Ponto flutuante  - Float
print(0.1 + 0.1)
print(2 * 0.2)
print('\n')

# Numeros arbitrarios - Python tenta encontrar uma forma de representar o resultado do modo mais extao possivel
print(0.2 + 0.1)
print(3 * 0.1)




